package thiago_xavier.parte2;

public class ConverteReal {

    double real, dolar;

    public void setReal(double r) {
        real = r;
    }

    public double getReal() {
        return real;
    }

    public void setDolar(double d) {
        dolar = d;
    }

    public double getDolar() {
        return dolar;
    }

}
